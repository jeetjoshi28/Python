# What is a Variable?

# A variable is a container used to store data in memory.

age = 25 
# Here age is a variable storing an integer value

name = "Jeet"
# Here name is a variable storing a string value

# | Data Type        | Description     | Example         |
# | ---------------- | --------------- | --------------- |
# | Integer (`int`)  | Whole numbers   | `10`, `-3`      |
# | Float (`float`)  | Decimal numbers | `3.14`, `2.5`   |
# | String (`str`)   | Text            | `"Hello"`       |
# | Boolean (`bool`) | True or False   | `True`, `False` |

price = 19.99 #float
is_logged_in = True #boolean
city = "New York" #string

# Rules for Variable Names
# Can contain letters, numbers, underscores
# can not start with a number
# No spaces

# Valid variable names:
total_price
user_name 

# Unvalid variable names:
# 2name
# user name 

